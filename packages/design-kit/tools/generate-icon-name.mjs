import { execSync } from 'node:child_process';
import { readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const currentDir = path.dirname(fileURLToPath(import.meta.url));
const iconSvgDir = path.resolve(currentDir, '../src/icon/svg');
const outputFile = path.resolve(currentDir, '../src/icon/icon-name.ts');

async function generateIconNameType() {
  const dirEntries = await readdir(iconSvgDir, { withFileTypes: true });

  const iconNames = dirEntries
    .filter(entry => entry.isFile() && entry.name.endsWith('.svg'))
    .map(entry => entry.name.replace(/\.svg$/u, ''))
    .sort((a, b) => a.localeCompare(b));

  const union =
    iconNames.length > 0
      ? iconNames.map(name => `'${name}'`).join(' | ')
      : 'never';

  const content = [
    '// This file is auto-generated by packages/design-kit/tools/generate-icon-name.mjs.',
    '// Do not edit manually.',
    '',
    `export type IconName = ${union};`,
    '',
  ].join('\n');

  await writeFile(outputFile, content, 'utf8');

  // Format the output using the workspace prettier CLI
  try {
    execSync(`npx prettier --write "${outputFile}"`, { stdio: 'pipe' });
  } catch (error) {
    // Formatting is a nice-to-have, don't fail the build if prettier isn't available
    console.warn('Could not format output with prettier:', error.message);
  }

  console.log(
    `Generated IconName type with ${iconNames.length.toString()} icons at ${path.relative(process.cwd(), outputFile)}`,
  );
}

await generateIconNameType();
